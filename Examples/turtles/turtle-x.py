# 거북이 이동 예제

import turtle

t1 = turtle.Pen()
t2 = turtle.Pen()

t1.forward(100)
t1.left(90)

t2.forward(100)
t2.right(90)

t1.forward(100)
t1.left(90)

t2.forward(100)
t2.right(90)

t1.forward(100)
t1.left(90)
t1.forward(100)

t2.forward(100)
t2.right(90)
t2.forward(100)

t1.right(90)
t1.forward(100)


turtle.done()